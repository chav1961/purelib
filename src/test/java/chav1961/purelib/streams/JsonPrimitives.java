package chav1961.purelib.streams;

public class JsonPrimitives {
	public int		x;
	public long	y;
	public float	z;
	public double	t;
	public boolean	a;
	public byte	b;
	public short	c;
	public char	d;

	public JsonPrimitives(){}
	
	@Override
	public String toString() {
		return "JsonPrimitives [x=" + x + ", y=" + y + ", z=" + z + ", t=" + t + ", a=" + a + "]";
	}
}
